import turtle as t
t.shape("turtle")

t.up()
t.goto(-200,-50)
t.down()
t.fillcolor("red")
t.begin_fill()
t.forward(400)
t.left(90)
t.forward(200)
t.left(90)
t.forward(400)
t.left(90)
t.forward(200)
t.end_fill()

t.up()
